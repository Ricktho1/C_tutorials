#include<stdio.h>
//print natural numbers from 0 to 10
int main(){
    //increment operator( Same for decrement operator)
    //++i(pre increment)---> increase then use
    //i++(post increment)---> use then increase
    int i;
    for( i=0 ;i<=10;i++)
    { printf("%d", i);}
    return 0;
    //for(i=1;;i++)//infinite loop
}